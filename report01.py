import turtle as t
t.shape("turtle")

t.pensize(10)
t.circle(100)                   

t.penup()
t.goto(-220,0)
t.color("dodgerblue")
t.pendown()
t.circle(100)                                        

t.penup()
t.goto(220,0)
t.color("red")
t.pendown()
t.circle(100)                   

t.penup()
t.goto(-110,-130)
t.color("gold")
t.pendown()
t.circle(100)                   

t.penup()
t.goto(110,-130)
t.color("green")
t.pendown()
t.circle(100)                   
